#include "ft_printf.h"

char			*tolower_str(char *str)
{
	int			i;

	i = 0;
	while (str[i])
	{
		if (ft_isalpha(str[i]))
			str[i] += 'a' - 'A';
		i++;
	}
	return (str);
}
